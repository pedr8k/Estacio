# 1. Criação da variável 'texto' com o valor 'Olá, laço for.'
texto = 'Olá, laço for.'

# 2. Laço 'for' que itera sobre cada caractere da variável 'texto'
for item in texto:
    print("Caractere: " + item)

# 3. Novo laço 'for' que itera sobre um intervalo numérico de 1 a 10
for num in range(1, 11):
    print("Número do intervalo: " + str(num))